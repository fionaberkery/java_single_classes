public class Calculator {

    public Calculator(){
    }

    // add
    public int addValues(int num1, int num2) {
         return (num1 + num2);
    }

    // subtract
    public int subtractValues(int num1, int num2) {
        return (num1 - num2);
    }

    // multiply
    public int multiplyValues(int num1, int num2){
        return (num1 * num2);
    }

   //divide
    public double divideValues(double num1, double num2){
        return (num1 / num2);
    }


}
